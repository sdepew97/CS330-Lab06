public class hash {
    public static void main(String args[]) {
        String s = "Bryn Mawr";
        System.out.println(s.hashCode());

        s = "K-Cass";
        System.out.println(s.hashCode());

        s = "Haverford";
        System.out.println(s.hashCode());

        s = "Swarthmore";
        System.out.println(s.hashCode());
    }
}
